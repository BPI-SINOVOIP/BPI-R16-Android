package com.broadcom.bt.map.mce;

import android.app.Activity;
import android.content.Intent;
import android.os.Bundle;
import android.util.Log;
import android.view.View;
import android.view.View.OnClickListener;
import android.widget.Button;
import android.widget.EditText;

public class CreateSMSActivity extends Activity implements OnClickListener {

    public static final String TAG = "CreateSMSActivity";
    public static final String EXTRAS_RECEIPIENT = "address";
    public static final String EXTRAS_MSGBODY = "msgbody";

    private EditText address;
    private EditText messageBody;
    private Button btnPushMessage;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        // TODO Auto-generated method stub
        super.onCreate(savedInstanceState);
        setContentView(R.layout.createsms);

        address = (EditText) findViewById(R.id.address);
        messageBody = (EditText) findViewById(R.id.msgbody);
        btnPushMessage = (Button) findViewById(R.id.btnPushMsg);
        btnPushMessage.setOnClickListener((OnClickListener) this);
    }

    @Override
    protected void onDestroy() {
        // TODO Auto-generated method stub
        super.onDestroy();
    }

    @Override
    protected void onResume() {
        // TODO Auto-generated method stub
        super.onResume();
    }

    @Override
    public void onClick(View v) {
        if(!address.getText().equals("") && !messageBody.getText().equals("")) {
            Log.d(TAG, "address = " + this.address.getText());
            Log.d(TAG, "msgbody = " + this.messageBody.getText());
            Intent data = new Intent();
            data.putExtra(EXTRAS_RECEIPIENT, this.address.getText().toString());
            data.putExtra(EXTRAS_MSGBODY, this.messageBody.getText().toString());

            setResult(RESULT_OK, data);
            finish();
        }

    }

}
